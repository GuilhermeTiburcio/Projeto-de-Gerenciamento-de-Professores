package br.com.alpha.tasks.domain;

public class Matter implements IDomain {
	private String name;
	private int id;
	
	public Matter(){}
	
	public Matter(String name, int id) {
		super();
		this.name = name;
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	@Override
	public int getId() {
		return id;
	}

	@Override
	public void setId(int id) {
		this.id = id;

	}

}
